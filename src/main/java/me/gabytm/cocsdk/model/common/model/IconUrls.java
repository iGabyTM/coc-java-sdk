package me.gabytm.cocsdk.model.common.model;

public class IconUrls {

    private String small;
    private String tiny;
    private String medium;

    public String getSmall() {
        return small;
    }

    public String getTiny() {
        return tiny;
    }

    public String getMedium() {
        return medium;
    }

    @Override
    public String toString() {
        return "IconUrls{" +
                "small='" + small + '\'' +
                ", tiny='" + tiny + '\'' +
                ", medium='" + medium + '\'' +
                '}';
    }

}
